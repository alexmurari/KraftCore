namespace KraftCore.Domain.Contracts
{
    /// <summary>
    ///     Directions which elements of a sequence can be sorted.
    /// </summary>
    public enum SortDirection
    {
        /// <summary>
        ///     Sorts the elements in ascending order.
        /// </summary>
        Ascending,

        /// <summary>
        ///     Sorts the elements in descending order.
        /// </summary>
        Descending
    }
}